/*Operadores de Comparacion*/

/*
 * == Comparacion
 * === Comparacion Estricta
 * != Diferente
 * !== Diferencia Estricta
 * > Mayor
 * < Menor
 * >= Mayor Igual
 * <= Menor Igual
 * */

const a = 10;
const b = 20;
const c = "10"

console.log(a == c);
console.log(a === c);
console.log(a >= b);
console.log(b > c);

/*Operadores Logicos*/

/*
 * && y
 * || O
 * ! Negacion
 */

